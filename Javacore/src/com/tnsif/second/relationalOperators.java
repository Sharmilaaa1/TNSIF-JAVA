package com.tnsif.second;

public class relationalOperators {

	public static void main(String[] args) {
		int a=6;
		int b=12;
		System.out.println("a is" +a + "and b is"+b);
		System.out.println("a==b:"+(a==b));
		System.out.println("a!=b:"+(a!=b));
		System.out.println("a<b:"+(a<b));
		System.out.println("a<=b:"+(a<=b));
		System.out.println("a>b:"+(a>b));
		System.out.println("a>=b:"+(a>=b));
	}
}
