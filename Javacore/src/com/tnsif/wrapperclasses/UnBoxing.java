package com.tnsif.wrapperclasses;
public class UnBoxing {
	public static void main(String[] args) {
		        // Example 1: Integer → int
		        Integer obj1 = 78;
		        int a = obj1;  
		        System.out.println("Unboxing Integer to int: " + a);
		        // Example 2: Character → char
		        Character obj2 = 'S';
		        char ch = obj2; 
		        System.out.println("Unboxing Character to char: " + ch);
		        // Example 3: Double → double
		        Double obj3 = 98.99;
		        double d = obj3;
		        System.out.println("Unboxing Double to double: " + d);
		    }
}
